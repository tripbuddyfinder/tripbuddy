package org.app.models;

public class GroupMsg {
private int gid,uid;
String msg,timeatamp;
public int getGid() {
	return gid;
}
public void setGid(int gid) {
	this.gid = gid;
}
public int getUid() {
	return uid;
}
public void setUid(int uid) {
	this.uid = uid;
}
public String getMsg() {
	return msg;
}
public void setMsg(String msg) {
	this.msg = msg;
}
public String getTimeatamp() {
	return timeatamp;
}
public void setTimeatamp(String timeatamp) {
	this.timeatamp = timeatamp;
}
}
